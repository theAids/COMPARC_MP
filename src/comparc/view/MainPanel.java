    private void commandcboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_commandcboxActionPerformed
        comm = commandcbox.getSelectedItem().toString();
        /*
         * disable unnecessary options
         */

        //String commandName = "";
        

        switch (comm) {
            case "DADDU":
            case "DMULT":
            case "OR":
            case "DSLLV":
            case "SLT":
                offsettxt.disable();
                rdcbox.enable();
                rscbox.enable();
                rtcbox.enable();
                break;
            case "DADDIU":
            case "ANDI":
            case "LWU":
            case "LW":
                offsettxt.enable();
                rdcbox.enable();
                rscbox.enable();
                rtcbox.disable();
                commtype = "I-Type";
                break;
            case "BNE":
            case "SW":
                offsettxt.enable();
                rdcbox.disable();
                rscbox.enable();
                rtcbox.enable();
                break;
            default:
                offsettxt.enable();
                rdcbox.disable();
                rscbox.disable();
                rtcbox.disable();
                break;
        }
        
        offsettxt.setText("");
        this.repaint();
        
        
    }//GEN-LAST:event_commandcboxActionPerformed

